/**
 * Base class that represents a query for the 'watchlist' table.
 *
 * 
 *
 * @method     WatchlistQuery orderById($order = Criteria::ASC) Order by the id column
 * @method     WatchlistQuery orderByUserId($order = Criteria::ASC) Order by the user_id column
 * @method     WatchlistQuery orderByItemId($order = Criteria::ASC) Order by the item_id column
